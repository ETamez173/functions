// The += essentially means (in your parlance) txt = txt + a + b. When dealing with a string,
//  it takes the value currently in the string and APPENDS the value after the += symbol
// += in JavaScript (as well as in many other languages) adds the right hand side to the 
// variable on the left hand side, storing the result in that variable.

function reverseString(str) {
    var newString = "";
    for (var i = str.length - 1; i >= 0; i--) {
        newString += str[i];
    }
    return newString
}
